using System;
namespace Group_Project.Classes
{
    /// <summary>
    /// A class to hold player details
    /// </summary>
    public class Player
    {
        /// <summary>
        /// The ID of the player
        /// </summary>
        public int PlayerID;
        /// <summary>
        /// The player's forname
        /// </summary>
        public string Forname;
        /// <summary>
        /// The player's surname
        /// </summary>
        public string Surname;
        /// <summary>
        /// The player's date of birth
        /// </summary>
        public DateTime DateOfBirth;
        /// <summary>
        /// The role the player has in the team
        /// </summary>
        public string Role;
    }
}
